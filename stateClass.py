class State():

    def __init__(self,stateId, varsTerms):
        self.stateId = stateId
        self.varsTerms = varsTerms

    def getId(self):
        return self.getId

    def setVarsTerms(self, varTerm):
        self.varsTerms.append(varTerm)

    def getVarsTerms(self):
        return self.varsTerms
